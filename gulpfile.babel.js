gulp.task('scripts', function(cb) {
pump([
    gulp.src([
      // Note: Since we are not using useref in the scripts build pipeline,
      //       you need to explicitly list your scripts here in the right order
      //       to be correctly concatenated
      './app/scripts/main.js',
      './app/scripts/backgroundEffect.js',
      './app/scripts/serviceWorker.js'
    ]),
    $.newer('.tmp/scripts'),
    $.sourcemaps.init(),
    $.babel(),
    $.sourcemaps.write(),
    gulp.dest('.tmp/scripts'),
    $.concat('main.min.js'),
    $.uglify(),
    // Output files,
    $.size({title: 'scripts'}),
    $.sourcemaps.write('.'),
    gulp.dest('dist/scripts'),
    gulp.dest('.tmp/scripts')
  ], cb)
});

// Scan your HTML for assets & optimize them
gulp.task('html', () => {
  return gulp.src('app/**/*.html')
    .pipe($.useref({
      searchPath: '{.tmp,app}',
      noAssets: true
    }))

    // Minify any HTML
    .pipe($.if('*.html', $.htmlmin({
      removeComments: true,
      collapseWhitespace: true,
      collapseBooleanAttributes: true,
      removeAttributeQuotes: true,
      removeRedundantAttributes: true,
      removeEmptyAttributes: true,
      removeScriptTypeAttributes: true,
      removeStyleLinkTypeAttributes: true,
      removeOptionalTags: true
    })))
    // Output files
    .pipe($.if('*.html', $.size({title: 'html', showFiles: true})))
    .pipe(gulp.dest('dist'));
});

// Clean output directory
gulp.task('clean', () => del(['.tmp', 'dist/*', '!dist/.git'], {dot: true}));

// Watch files for changes & reload
gulp.task('default', ['scripts', 'styles_dev'], () => {
  browserSync({
    notify: false,
    // Customize the Browsersync console logging prefix
    logPrefix: 'WSK',
    // Allow scroll syncing across breakpoints
    scrollElementMapping: ['main'],
    // Run as an https by uncommenting 'https: true'
    // Note: this uses an unsigned certificate which on first access
    //       will present a certificate warning in the browser.
    // https: true,
    server: ['.tmp', 'app'],
    port: 3000
  });

  gulp.watch(['app/**/*.html'], reload);
  gulp.watch(['app/styles/**/*.{scss,css}'], ['styles_dev', reload]);
  gulp.watch(['app/scripts/**/*.js'], ['scripts', reload]);
  gulp.watch(['app/images/**/*'], reload);
});

// Build and serve the output from the dist build
gulp.task('serve:dist', ['deploy'], () =>
  browserSync({
    notify: false,
    logPrefix: 'WSK',
    scrollElementMapping: ['main'],
    server: 'dist',
    port: 3001
  })
);

// Build production files, the default task
gulp.task('deploy', ['clean'], cb =>
  runSequence(
    'styles',
    ['html', 'scripts', 'images', 'copy'],
    'generate-service-worker',
    cb
  )
);

